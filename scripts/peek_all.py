def peek(symbol):
  dat = yf.Ticker(symbol)
  info = [symbol] + [dat.info[x] if x in dat.info else None for x in ['beta', 'marketCap', 'sector', 'previousClose',  'forwardEps', 'dividendYield', 'payoutRatio', 'forwardPE', 'trailingPE', 'averageAnalystRating', 'overallRisk']]
  return info

